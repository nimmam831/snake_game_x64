//How to play:
//F is the fruit,
//O is the head,
//o's are the tail
//To get a tail, touch the F.
//It will spawn, and after it does, and you touch more, it gets longer.
//#'s are the walls,
//If you touch the wall, dont worry! You will not die, but go through the other side.
//If you touch the tail, dont worry! You will not die, but your score will go lower and the tail gets shorter.
//W/w: up,
//A/a: left,
//S/s: down,
//D/d: right
